func BenchmarkComputeH(b *testing.B) {
	const n = 1000000
	A := make([]curve.Element, n)
	B := make([]curve.Element, n)
	C := make([]curve.Element, n)
	for i := 0; i < n; i++ {
		A[i].SetRandom()
		B[i].SetRandom()
		C[i].SetRandom()
	}

	b.ResetTimer()
	for i := 0; i < b.N; i++ {
		<-computeH(A, B, C, n+3)
	}

}
