	b2 := e.fp.Sub(&c1.A0, &c1.A1)
	b6 := &c0.A1
	b8 := &c1.A1
	mone := e.fp.NewElement(-1)

	// d0  =  a0 b0  - 2 * (a4 b8 + a6 b6 + a10 b2 + a9) - 4 * a10 b8
