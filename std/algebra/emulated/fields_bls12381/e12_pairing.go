func (e Ext12) nSquare(z *E12, n int) *E12 {
	for i := 0; i < n; i++ {
		z = e.Square(z)
	}
	return z
}

func (e Ext12) nSquareGS(z *E12, n int) *E12 {
	for i := 0; i < n; i++ {
		z = e.CyclotomicSquareGS(z)
	}
	return z
}

// ExptNeg sets z to x^t in E12 and return z
// where t = -u = 15132376222941642752
func (e Ext12) ExptNeg(x *E12) *E12 {
	// Expt computation is derived from the addition chain:
	//
	//	_10      = 2*1
	//	_11      = 1 + _10
	//	_1100    = _11 << 2
	//	_1101    = 1 + _1100
	//	_1101000 = _1101 << 3
	//	_1101001 = 1 + _1101000
	//	return     ((_1101001 << 9 + 1) << 32 + 1) << 15
	//
	// Operations: 62 squares 5 multiplies
	//
	// Generated by github.com/mmcloughlin/addchain v0.4.0.

	z := e.Square(x)
	z = e.Mul(x, z)
	z = e.nSquare(z, 2)
	z = e.Mul(x, z)
	z = e.nSquare(z, 3)
	z = e.Mul(x, z)
	z = e.nSquare(z, 9)
	z = e.Mul(x, z)
	z = e.nSquare(z, 32)
	z = e.Mul(x, z)
	z = e.nSquare(z, 15)
	z = e.Square(z)

	return z
}

// ExptGS sets z to x^t in E12 and return z
// where t = u = -15132376222941642752
func (e Ext12) ExptGS(x *E12) *E12 {
	// ExptGS computation is derived from the addition chain:
	//
	//	_10      = 2*1
	//	_11      = 1 + _10
	//	_1100    = _11 << 2
	//	_1101    = 1 + _1100
	//	_1101000 = _1101 << 3
	//	_1101001 = 1 + _1101000
	//	return     ((_1101001 << 9 + 1) << 32 + 1) << 15
	//
	// Operations: 62 squares 5 multiplies
	//
	// Generated by github.com/mmcloughlin/addchain v0.4.0.
	z := e.ExptHalfGS(x)
	z = e.CyclotomicSquareGS(z)
	return z
}

func (e Ext12) ExptHalfGS(x *E12) *E12 {
	z := e.CyclotomicSquareGS(x)
	z = e.Mul(x, z)
	z = e.nSquareGS(z, 2)
	z = e.Mul(x, z)
	z = e.nSquareGS(z, 3)
	z = e.Mul(x, z)
	z = e.nSquareGS(z, 9)
	z = e.Mul(x, z)
	z = e.nSquareGS(z, 32)
	z = e.Mul(x, z)
	z = e.nSquareGS(z, 15)
	z = e.Conjugate(z)

	return z
}

// MulBy02368 multiplies a by an E12 sparse element b of the form
