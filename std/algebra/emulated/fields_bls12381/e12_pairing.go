// Mul02368By02368ThenMul computes a · (l0 · l1) where l0 and l1 are sparse lines.
// First multiplies the two sparse lines together (sparse × sparse = semi-sparse),
// then multiplies the result by a.
// The product of two sparse lines has zeros at positions 1 and 7.
func (e *Ext12) Mul02368By02368ThenMul(a *E12, c0_0, c1_0, c0_1, c1_1 *E2) *E12 {
	// Extract line coefficients for l0
	// l0 = b0 + b2*w² + 1*w³ + b6*w⁶ + b8*w⁸
	b0 := e.fp.Sub(&c0_0.A0, &c0_0.A1)
	b2 := e.fp.Sub(&c1_0.A0, &c1_0.A1)
	// b3 = 1 (implicit)
	b6 := &c0_0.A1
	b8 := &c1_0.A1

	// Extract line coefficients for l1
	// l1 = d0 + d2*w² + 1*w³ + d6*w⁶ + d8*w⁸
	d0 := e.fp.Sub(&c0_1.A0, &c0_1.A1)
	d2 := e.fp.Sub(&c1_1.A0, &c1_1.A1)
	// d3 = 1 (implicit)
	d6 := &c0_1.A1
	d8 := &c1_1.A1

	// Compute L = l0 · l1 (sparse × sparse)
	// L has non-zero coefficients at positions 0,2,3,4,5,6,8,9,10,11
	// Positions 1 and 7 are ZERO
	mone := e.fp.NewElement(-1)
	one := e.fp.One()

	// L_0 = b0*d0 - 2*b6*d6
	L0 := e.fp.Eval([][]*baseEl{{b0, d0}, {mone, b6, d6}}, []int{1, 2})

	// L_2 = b0*d2 + b2*d0 - 2*(b6*d8 + b8*d6)
	L2 := e.fp.Eval([][]*baseEl{{b0, d2}, {b2, d0}, {mone, b6, d8}, {mone, b8, d6}}, []int{1, 1, 2, 2})

	// L_3 = b0*1 + 1*d0 = b0 + d0
	L3 := e.fp.Add(b0, d0)

	// L_4 = b2*d2 - 2*b8*d8
	L4 := e.fp.Eval([][]*baseEl{{b2, d2}, {mone, b8, d8}}, []int{1, 2})

	// L_5 = b2*1 + 1*d2 = b2 + d2
	L5 := e.fp.Add(b2, d2)

	// L_6 = b0*d6 + 1*1 + b6*d0 + 2*b6*d6
	// L_6 = b0*d6 + b6*d0 + 2*b6*d6 + 1
	L6 := e.fp.Eval([][]*baseEl{{b0, d6}, {b6, d0}, {b6, d6}, {one}}, []int{1, 1, 2, 1})

	// L_8 = b0*d8 + b2*d6 + b6*d2 + b8*d0 + 2*b6*d8 + 2*b8*d6
	L8 := e.fp.Eval([][]*baseEl{{b0, d8}, {b2, d6}, {b6, d2}, {b8, d0}, {b6, d8}, {b8, d6}}, []int{1, 1, 1, 1, 2, 2})

	// L_9 = 1*d6 + b6*1 = b6 + d6
	L9 := e.fp.Add(b6, d6)

	// L_10 = b2*d8 + b8*d2 + 2*b8*d8
	L10 := e.fp.Eval([][]*baseEl{{b2, d8}, {b8, d2}, {b8, d8}}, []int{1, 1, 2})

	// L_11 = 1*d8 + b8*1 = b8 + d8
	L11 := e.fp.Add(b8, d8)

	// Now multiply a by L (semi-sparse multiplication)
	// L has zeros at positions 1 and 7
	return e.MulBySemiSparse1_7(a, L0, L2, L3, L4, L5, L6, L8, L9, L10, L11)
}

